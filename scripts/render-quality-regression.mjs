// ===== DOM pxf chrome: interactive cards/popups stay on the pixel-frame kit =====
// The DOM design system is the pxf frame family generated by src/ui/pixelKit.ts
// (the .pxf-close chamfered corners are the owner's gold standard). Two bounded
// checks keep it that way without tripping on legitimate legacy styles:
//   1. A curated list of interactive card/popup selectors must wear a generated
//      --pxf-* frame and end the cascade with hard corners (radius <= 2px).
//   2. Rounded chrome (border-radius > 2px) is frozen to the inventory below —
//      selectors that are either dead legacy CSS or overridden to 0 by the
//      PIXEL RESKIN section. NEVER add to these lists: new interactive chrome
//      must join the pxf family instead.
function parseCssRules(source) {
  const rules = [];
  const stripped = source.replace(/\/\*[\s\S]*?\*\//g, '');
  for (const match of stripped.matchAll(/([^{}]+)\{([^{}]*)\}/g)) {
    const selectors = match[1].trim().replace(/\s+/g, ' ')
      .split(',').map(part => part.trim()).filter(Boolean);
    if (selectors.length > 0) rules.push({ selectors, body: match[2] });
  }
  return rules;
}
function maxRadiusPx(value) {
  let max = 0;
  for (const [, num, unit] of value.matchAll(/(-?\d*\.?\d+)(px|%|em|rem)?/g)) {
    // Any %-/em-based rounding on chrome is treated as smooth (over budget).
    max = Math.max(max, unit && unit !== 'px' ? 999 : Number(num));
  }
  return max;
}

const appCssRules = parseCssRules(cssSource);
const pxfFramedSelectors = new Set();
const finalRadiusBySelector = new Map(); // last declaration wins (cascade order)
for (const rule of appCssRules) {
  const hasPxfFrame = /var\(--pxf-/.test(rule.body);
  const radiusDecls = [...rule.body.matchAll(/border-radius\s*:\s*([^;]+)/g)];
  for (const selector of rule.selectors) {
    if (hasPxfFrame) pxfFramedSelectors.add(selector);
    for (const decl of radiusDecls) finalRadiusBySelector.set(selector, maxRadiusPx(decl[1]));
  }
}

